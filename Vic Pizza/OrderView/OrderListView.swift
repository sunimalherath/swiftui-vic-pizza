import SwiftUI

struct OrderListView: View {
    var orderModel: OrderModel
    var body: some View {
        VStack {
            ListHeaderView(orderModel: orderModel, text: "Your Order")
            
            List(orderModel.orders) { item in
                OrderRowView(orderItem: item)
            }
        }
    }
}

